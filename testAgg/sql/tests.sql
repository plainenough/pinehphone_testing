CREATE TABLE TestRuns (
id int UNSIGNED AUTO_INCREMENT,
device_id INT UNSIGNED NOT NULL,
os_id INT UNSIGNED NOT NULL,
tester_id INT UNSIGNED NOT NULL,
testcase_id INT UNSIGNED NOT NULL,
results CHAR(4),
notes VARCHAR(256) DEFAULT 'None supplied',
testrun INT NOT NULL,
reg_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
PRIMARY KEY (id),
FOREIGN KEY (tester_id) REFERENCES Testers(id),
FOREIGN KEY (testcase_id) REFERENCES TestBank(id),
FOREIGN KEY (device_id) REFERENCES Devices(id),
FOREIGN KEY (os_id) REFERENCES OS(id) ); 
